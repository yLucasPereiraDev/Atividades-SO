#Declaração de Variáveis
Comprimento: int = 0
Largura: int = 0
Altura: int = 0
Volume: int = 0

#Início
print("Volume do Paralelepípedo")
Comprimento = int(input("Digite o comprimento do paralelepípedo: "))
Largura = int(input("Digite a largura do paralelepípedo: "))
Altura = int(input("Digite a altura do paralelepípedo: "))

Volume = (Comprimento*Largura*Altura)
print("O volume do Paralelepípedo é igual a: ", Volume)
