#Declaração de Variáveis
N1: int = 0 
N2: int = 0
SN: int = 0

#Início
print ("Soma dos quadrados")
N1 = int(input("Digite o valor do primeiro número: "))
N2 = int(input("Digite o valor do segundo número: "))

N1 = (N1*N1)
N2 = (N2*N2)
SN = (N1+N2)

print ("O quadrado do N1 é: ", N1)
print ("O quadrado do N2 é: ", N2)
print ("A soma dos quadrados dos números inseridos é: ", SN)
